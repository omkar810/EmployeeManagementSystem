package com.company;

public class Employee {
	int id;
	String name;
	int age;
	double salary;
	int deptno;
	String job;

	Employee(int id, String name,int age, double salary, int deptno, String job) {
		this.id = id;
		this.name = name;
		this.age=age;
		this.salary = salary;
		this.deptno = deptno;
		this.job = job;
	}
}
